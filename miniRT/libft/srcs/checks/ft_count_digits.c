int	ft_count_digits(long long n, int base)
{
	int	i;

	if (n == 0)
		return (1);
	i = 0;
	if (n < 0)
	{
		i++;
		n = -n;
	}
	while (n)
	{
		n /= base;
		i++;
	}
	return (i);
}
