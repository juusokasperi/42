#include "libft.h"

void	*ft_realloc(void *ptr, size_t old_size, size_t new_size)
{
	void	*new_ptr;

	if (new_size == 0)
		return (ft_free(&ptr));
	new_ptr = malloc(new_size);
	if (!new_ptr)
		return (ft_free(&ptr));
	if (ptr)
	{
		if (old_size < new_size)
			ft_memcpy(new_ptr, ptr, old_size);
		else
			ft_memcpy(new_ptr, ptr, new_size);
		ft_free(&ptr);
	}
	return (new_ptr);
}
