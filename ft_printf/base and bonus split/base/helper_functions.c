#include "ft_printf.h"

int	is_type(char c)
{
	if (c == '%' || c == 'c' || c == 's' || c == 'p' || c == 'i'
		|| c == 'd' || c == 'u' || c == 'x' || c == 'X')
		return (1);
	return (0);
}

int	is_digit(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
